/**
 * ============================================================================
 * ИЗМЕНЕНИЯ М2: Функция управления состоянием кнопок
 * ============================================================================
 * Назначение: Блокирует кнопки редактирования, если нет загруженных файлов
 * 
 * Как это работает:
 * 1. Проверяет массив openFiles на наличие файлов
 * 2. Если файлов нет - устанавливает атрибут disabled для кнопок редактирования
 * 3. Если файлы есть - убирает атрибут disabled, делая кнопки активными
 * 
 * Визуальное оформление неактивных кнопок находится в styles-changes-m2.css
 */

/**
 * ============================================================================
 * ФУНКЦИЯ: updateButtonsState()
 * ============================================================================
 * Обновляет состояние кнопок в зависимости от наличия открытых файлов
 * 
 * @requires {Array} openFiles - глобальный массив с загруженными файлами
 * @requires {DOM} .tab-action-btn - класс кнопок действий
 * @requires {DOM} .ribbon-btn - класс кнопок ленты
 * 
 * Логика работы:
 * - hasFiles = true  → все кнопки активны (disabled удалён)
 * - hasFiles = false → кнопки редактирования заблокированы (disabled установлен)
 * - Кнопки "Открыть" и "Сохранить" исключены из блокировки через :not() селектор
 * 
 * Когда вызывать:
 * 1. При первой загрузке страницы (DOMContentLoaded)
 * 2. После добавления файла в openFiles
 * 3. После удаления файла из openFiles
 * 4. После полной очистки массива openFiles
 */
function updateButtonsState() {
    // =========================================================================
    // ШАГ 1: Проверка наличия загруженных файлов
    // =========================================================================
    // Проверяем, что массив openFiles существует и содержит хотя бы один элемент
    // Результат: true (файлы есть) или false (файлов нет)
    const hasFiles = openFiles && openFiles.length > 0;
    
    // =========================================================================
    // ШАГ 2: Получение списка кнопок для управления
    // =========================================================================
    // Селекторы кнопок, которые должны блокироваться при отсутствии файлов:
    // - .tab-action-btn - кнопки в панели действий
    // - .ribbon-btn - кнопки в ленте инструментов
    // Исключения (остаются активными всегда):
    // - :not(.btn-open) - кнопка "Открыть"
    // - :not(.btn-save) - кнопка "Сохранить"
    //
    // ПРИМЕЧАНИЕ: Для добавления других кнопок в список исключений,
    // добавьте их классы через :not(.ваш-класс)
    const editableButtons = document.querySelectorAll(`
        .tab-action-btn:not(.btn-open):not(.btn-save),
        .ribbon-btn:not(.btn-open):not(.btn-save)
    `);
    
    // =========================================================================
    // ШАГ 3: Применение состояния к каждой кнопке
    // =========================================================================
    // Проходим по всем найденным кнопкам и устанавливаем/снимаем блокировку
    editableButtons.forEach(button => {
        if (hasFiles) {
            // Файлы загружены → разблокируем кнопку
            // removeAttribute полностью удаляет атрибут disabled
            button.removeAttribute('disabled');
        } else {
            // Файлов нет → блокируем кнопку
            // setAttribute устанавливает disabled="true", делая кнопку неактивной
            button.setAttribute('disabled', 'true');
        }
    });
    
    // =========================================================================
    // ОТЛАДКА (опционально, можно раскомментировать для тестирования)
    // =========================================================================
    // console.log('updateButtonsState:', {
    //     hasFiles: hasFiles,
    //     filesCount: openFiles ? openFiles.length : 0,
    //     buttonsFound: editableButtons.length,
    //     state: hasFiles ? 'ACTIVE' : 'DISABLED'
    // });
}

// ============================================================================
// ТОЧКИ ВЫЗОВА ФУНКЦИИ
// ============================================================================
// Ниже приведены места, где необходимо вызвать updateButtonsState()
// для корректной работы функционала

// -----------------------------------------------------------------------------
// ТОЧКА 1: При загрузке страницы
// -----------------------------------------------------------------------------
// Вызывается сразу после загрузки DOM, чтобы установить начальное состояние
// кнопок (заблокировано, если файлов ещё нет)
document.addEventListener('DOMContentLoaded', () => {
    updateButtonsState();
});

